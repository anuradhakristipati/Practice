public class LogicalOperators {
	
	public static void main(String []args) {
	
		int a = 10;
		int b = 5;
		int y = 10;
		int x = 6;
		
	
		System.out.println(a>b);
		System.out.println(y>x);
		System.out.println(a>b && y<x);
		
		System.out.println(a>b || y<x);
		
		System.out.println(!(a<b));


	
	}
}